package havis.middleware.ale.core.field;

/**
 * Build-in formats specified in ALE 1.1 (6.2)
 */
public enum FieldFormat {

	/**
	 * epc-pure format
	 */
	EPC_PURE,

	/**
	 * epc-tag format
	 */
	EPC_TAG,

	/**
	 * epc-hex format
	 */
	EPC_HEX,

	/**
	 * epc-decimal format
	 */
	EPC_DECIMAL,

	/**
	 * hex format
	 */
	HEX,

	/**
	 * decimal format
	 */
	DECIMAL,

	/**
	 * string format
	 */
	STRING
}